export function inferMoonshotGoalKeyFromTheme(theme: MoonshotThemeSource): string | null {
  if (theme.worldviewCardId && MOONSHOT_GOAL_KEYS.includes(theme.worldviewCardId)) {
    return theme.worldviewCardId
  }

  const text = `${theme.title || ''} ${theme.description || ''}`
  const numberMatch = text.match(/目標\s*([1237])/)
  if (numberMatch) return GOAL_NUMBER_TO_KEY[numberMatch[1]] || null

  if (text.includes('身体・脳・空間・時間')) return 'goal1'
  if (text.includes('超早期') || text.includes('予測予防')) return 'goal2'
  if (text.includes('共進化') || text.includes('ロボット')) return 'goal3'
  if (text.includes('100歳') || text.includes('健康不安')) return 'goal7'

  return null
}

